//Existen operadores para comparacion como 

let a = 5;

//Comparadores de mayor menor o igual
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

console.log(" ");
//Comparadores de igualdad solo comparan el dato
console.log(a == 5);
console.log(a != 5);

console.log(" ");
//Comparadores de igualdad comparan tanto el dato como tipo de dato, "MAS SEGURIDAD"
console.log(a === '5');
console.log(a !== '5');
