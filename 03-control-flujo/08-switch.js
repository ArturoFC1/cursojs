//La creacion de un switch en JS se utiliza para validar opciones ingresadas ejemplo
let accion = 'volando';

switch(accion){
    case 'caminar':
        console.log("Esta caminando");
        break;
    case 'correr':
        console.log("Esta corriendo");
        break;
    default:
        console.log("No se sabe");
        break;
}